function binaryAgent(str) {
  var str_split=str.split(" ");
  var tr=[];
  for (var ct=0;ct<str_split.length;ct++){
   var curr=str_split[ct];
   var a=parseInt(curr,2);
   tr.push(String.fromCharCode(a));
  }
  return tr.join("");
}

